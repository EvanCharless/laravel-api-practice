// to retreive token from laravel sanctum manually
Route::get('/setup', function () {
    $credentials = [
        'email' => 'admin@admin.com',
        'password' => 'password'
    ];

    if (!Auth::attempt($credentials)) {
        $user = new \App\Models\User();

        $user->name = 'Admin';
        $user->email = $credentials['email'];
        $user->password = Hash::make($credentials['password']);

        $user->save();

        if (Auth::attempt($credentials)) {
            /** @var \App\Models\User $user **/
            $user = Auth::user();

            // To assign each role's capabilities
            $adminToken = $user->createToken('admin-token'); // default = ['*'] / which is means can access all
            $updateToken = $user->createToken('admin-token', ['create', 'update']);
            $basicToken = $user->createToken('admin-token', ['read']);

            return [
                'admin' => $adminToken->plainTextToken,
                'update' => $updateToken->plainTextToken,
                'basic' => $basicToken->plainTextToken,
            ];
        };
    };
});
